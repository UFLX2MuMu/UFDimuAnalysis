
#include "SlimJetInfo.h"

void SlimJetInfo::init() {

  pt       = -999;
  eta      = -999;
  phi      = -999;
  mass     = -999;
  partonID = -999;
  
  jecFactor = -999;
  jecUnc    = -999;
  
  CSV  = -999;
  puID = -999;

} // End void SlimJetInfo::init()

///////////////////////////////////////////////////////////
//--------------------------------------------------------
///////////////////////////////////////////////////////////

Float_t SlimJetInfo::getMass()
{
    return mass;
}

///////////////////////////////////////////////////////////
//--------------------------------------------------------
///////////////////////////////////////////////////////////

TLorentzVector SlimJetInfo::get4vec()
{
    TLorentzVector v;
    v.SetPtEtaPhiM(pt, eta, phi, mass);
    return v;
}

///////////////////////////////////////////////////////////
//--------------------------------------------------------
///////////////////////////////////////////////////////////

TString SlimJetInfo::outputInfo()
{
    TString s = Form("pt: %7.3f, eta: %7.3f, phi: %7.3f, mass: %7.3f", 
                      pt, eta, phi, mass);
    return s;
}

///////////////////////////////////////////////////////////
//--------------------------------------------------------
///////////////////////////////////////////////////////////

Double_t SlimJetInfo::iso()
{
    return 0.0;
}
